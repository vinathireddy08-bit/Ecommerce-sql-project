# 🛒 E-Commerce SQL Data Analysis Project

## 📌 Project Overview
This project is a **comprehensive E-Commerce Database Analysis system** built using SQL.  
It demonstrates how relational databases can be designed and used to manage, analyze, and extract meaningful business insights from transactional data.

The project focuses on:
- Designing a structured relational database
- Writing SQL scripts for data insertion
- Performing business analysis using SQL queries
- Visualizing insights using dashboards and query outputs

