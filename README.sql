## 📊 Business Analysis Objectives

This project answers key real-world business questions:

- 📈 Total revenue generated
- 🧑‍💼 Top customers based on spending
- 📦 Best-selling products
- 🛒 Monthly sales performance
- 🏷️ Category-wise revenue distribution
- 📉 Identification of low-performing products

