## 🗄️ Database Schema Design
The database is designed using proper normalization techniques to ensure data integrity and efficiency.

### 📌 Core Tables:
- Customers
- Products
- Orders
- Order_Items
- Payments

### 🔗 Relationships:
- A customer can place multiple orders  
- Each order contains multiple order items  
- Products are linked to order items  
- Payments are associated with orders  

